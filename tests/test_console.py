class TestConsole(unittest.TestCase):
    """Test the console"""
    @classmethod
    def setUpClass(self):
        """Set up class"""
        try:
            os.rename("file.json", "tmp")
        except IOError:
            pass
        FileStorage.__objects = {}

    @classmethod
    def tearDownClass(self):
        """Tear down class"""
        try:
            os.remove("file.json")
        except IOError:
            pass
        try:
            os.rename("tmp", "file.json")
        except IOError:
            pass

    def create(self):
        """Create an instance of HBNBCommand"""
        self.backup = sys.__stdout__
        self.file = StringIO()
        sys.stdout = self.file
        return HBNBCommand()

    def test_quit(self):
        """Test that quit implementation exists"""
        console = self.create()
        self.assertTrue(console.onecmd("quit"))
        sys.stdout = self.backup

    def test_EOF(self):
        """Test that EOF implementation exists"""
        console = self.create()
        self.assertTrue(console.onecmd("EOF"))
        sys.stdout = self.backup

    def test_help(self):
        expected = ['\n', 'Documented commands (type help <topic>):',
                    '\n', '========================================',
                    '\n', 'EOF  all  count  create  destroy  help  ',
                    'quit  show  update', '\n']
        console = self.create()
        console.onecmd('help')
        self.assertEqual(self.file.getvalue()[:-1], "".join(expected))
        sys.stdout = self.backup

    def test_empty_line(self):
        """Test that emptyline executes nothing"""
        console = self.create()
        console.onecmd('\n')
        self.assertTrue(len(self.file.getvalue()) == 0)
        sys.stdout = self.backup

